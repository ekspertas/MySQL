/* Задание # 1.
 	Подсчитайте средний возраст пользователей в таблице users.
*/

SELECT
  ROUND(									-- округляем до целого числа
  AVG(										-- вычисляем среднее значение
  TIMESTAMPDIFF (YEAR, birthday_at, NOW())	-- вычисляем возраст пользователя
     ))
   AS 'Средний возраст пользователей'		-- задаем название колонки
  FROM
  users;									-- таблица из кторой берём данные

